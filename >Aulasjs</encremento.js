let y = 5;

// Pos-incremento
console.log(y++); // exibe 5 ( usa o valor atual, depopis incrementa )
console.log(y);// agora y e 6 

//pre-incremento

console.log(++y);// incrementa primeiro  e depois usa o valor (7)


// pos- decremento 
console.log(y--); // exibe 7 ( usa o valor atual e depois decrementa )
console.log(y);// agora o y  e 6 


// pos decremento 

console.log(--y); // decrementa primeiro e depois usa o valor (5)
